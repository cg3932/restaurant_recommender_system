package g9_rest_recommender_final;
import javax.swing.*;
import javax.swing.event.*;
import java.awt.*;

/**
 *
 * @author Christian
 */
public class open_web_url {
    // Used to open a website from within the program
    public void open_url (String weburl){
        String[] cmd = new String[4];

        cmd[0] = "cmd.exe";
        cmd[1] = "/C";
        cmd[2] = "start";
        cmd[3] = weburl;

        try {
            // open wb url
            Process p = Runtime.getRuntime().exec(cmd);
        } catch (Exception exc) {
            // an error has occured
        }
    }
}
